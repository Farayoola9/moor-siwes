# A Palindrome 
print("Hint: You are to input a word that can retain its normal spelling after reversing")
word = str(input("Enter a string: "))
reverse = word[::-1]
print(reverse)

if word == reverse:
    print("Its palindrome")
else:
    print("Its not palindrome")




